package Atividade;

public class Exercicio_numeros_positivos {
    public static void main(String[] args) {
        int i=1;

        while (i<=10) {
            System.out.println(i);
            i++;
            
            if (i>10) {
                System.out.println("número positivo");
            } else {
                System.out.println("número negativo");
                

            }
         
        }


    }
}
